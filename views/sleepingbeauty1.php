<h1>
  La Belle au Bois Dormant
  <br><br>
  Conte
</h1>

<p>Il était une fois un Roi et une Reine, qui étaient si fâchés de n'avoir point d'enfants, si fâchés qu'on ne saurait dire. 
Ils allèrent à toutes les eaux du monde ; voeux, pèlerinages, menues dévotions, tout fut mis en oeuvre, et rien n'y faisait. 
Enfin pourtant la Reine devint grosse, et accoucha d'une fille : on fit un beau Baptême ; on donna pour Marraines à la petite Princesse 
toutes les Fées qu'on pût trouver dans le Pays (il s'en trouva sept), afin que chacune d'elles lui faisant un don, comme c'était la coutume 
des Fées en ce temps-là, la Princesse eût par ce moyen toutes les perfections imaginables. Après les cérémonies du Baptême toute la compagnie 
revint au Palais du Roi, où il y avait un grand festin pour les Fées.</p>