<h1>
    La Barbe Bleue
    <br><br>
    Conte
</h1>

<p>Il était une fois un homme qui avait de belles maisons à la ville et à la Campagne, de la vaisselle d'or et d'argent, des meubles 
en broderie, et des carrosses tout dorés ; mais par malheur cet homme avait la Barbe bleue : cela le rendait si laid et si terrible, 
qu'il n'était ni femme ni fille qui ne s'enfuît de devant lui.</p>

<p>Une de ses Voisines, Dame de qualité, avait deux filles parfaitement belles. Il lui en demanda une en Mariage, et lui laissa le choix 
de celle qu'elle voudrait lui donner. Elles n'en voulaient point toutes deux, et se le renvoyaient l'une à l'autre, ne pouvant se résoudre 
à prendre un homme qui eût la barbe bleue.</p>