<p>On mit devant chacune d'elles un couvert magnifique, avec un étui d'or massif, où il y avait une cuiller une fourchette, et un couteau 
de fin or garni de diamants et de rubis.Mais comme chacun prenait sa place à table, on vit entrer une vieille Fée qu'on n'avait point priée 
parce qu'il y avait plus de cinquante ans qu'elle n'était sortie d'une Tour et qu'on la croyait morte, ou enchantée. Le Roi lui fit donner 
un couvert, mais il n'y eut pas moyen de lui donner un étui d'or massif, comme aux autres, parce que l'on n'en avait fait faire que sept 
pour les sept Fées.</p>

<p>La vieille crut qu'on la méprisait, et grommela quelques menaces entre ses dents. Une des jeunes Fées qui se trouva 
auprès d'elle l'entendit, et jugeant qu'elle pourrait donner quelque fâcheux don à la petite Princesse, alla dès qu'on fut sorti de table 
se cacher derrière la tapisserie, afin de parler la dernière, et de pouvoir réparer autant qu'il lui serait possible le mal que la vieille 
aurait fait...</p>