<h1>Cendrillon
<br>
La petite pantoufle de verre
<br><br>
Conte</h1>

<p>Il était une fois un Gentilhomme qui épousa en secondes noces une femme, la plus hautaine et la plus fière qu'on eût jamais vue. 
Elle avait deux filles de son humeur, et qui lui ressemblaient en toutes choses. Le Mari avait de son côté une jeune fille, mais 
d'une douceur et d'une bonté sans exemple ; elle tenait cela de sa Mère, qui était la meilleure personne du monde. Les noces ne furent 
pas plus tôt faites, que la Belle-mère fit éclater sa mauvaise humeur; elle ne put souffrir les bonnes qualités de cette jeune enfant, 
qui rendaient ses filles encore plus haïssables.</p>