<h1>
    Le Chat botté
    <br><br>
    Conte
</h1>

<p>Un Meunier ne laissa pour tous biens à trois enfants qu'il avait, que son Moulin, son âne, et son chat. Les partages furent bientôt faits, 
ni le Notaire, ni le Procureur n'y furent point appelés. Ils auraient eu bientôt mangé tout le pauvre patrimoine. L'aîné eut le Moulin, 
le second eut l'âne, et le plus jeune n'eut que le Chat. Ce dernier ne pouvait se consoler d'avoir un si pauvre lot :
Mes frères, disait-il, pourront gagner leur vie honnêtement en se mettant ensemble ; pour moi, lorsque j'aurai mangé mon chat, et que je 
me serai fait un manchon de sa peau, il faudra que je meure de faim.</p>



